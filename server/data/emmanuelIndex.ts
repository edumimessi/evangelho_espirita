/**
 * Retorna o índice de comentários de Emmanuel para um capítulo específico.
 * Formato: { [verse: string]: EmmanuelRef[] }
 */
export function getEmmanuelChapterIndex(
  bookAbbrev: string,
  chapter: number
): Record<string, EmmanuelRef[]> {
  const bookData = emmanuelIndex[bookAbbrev.toLowerCase()];
  if (!bookData) return {};
  const chapterData = bookData[chapter];
  if (!chapterData) return {};
  const result: Record<string, EmmanuelRef[]> = {};
  for (const [verse, refs] of Object.entries(chapterData)) {
    result[String(verse)] = refs;
  }
  return result;
}
